package carte;

public class Montagne {
	    private int x;
		private int y;

	    public Montagne(int x, int y) {
	        this.x = x;
	        this.y = y;
	    }
	    
	 // Getters et Setters pour les attributs de la montagne

	    public int getX() {
	        return x;
	    }
	    
	    public void setX(int x) {
			this.x = x;
		}

	    public int getY() {
	        return y;
	    }
	    
	    public void setY(int y) {
			this.y = y;
		}
}
